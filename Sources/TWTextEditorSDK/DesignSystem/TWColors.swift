import UIKit

extension UIColor {
    static var twPlaceHolderColor: UIColor {
        return UIColor.hexStringToUIColor(hex: "#5E6160")
    }
    
    static var twBorderColor: UIColor {
        return UIColor.hexStringToUIColor(hex: "#EDEDED")
    }
    
}
